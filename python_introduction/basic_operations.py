#Define variables
number1 = 10
number2 = 5
# Sum of number1 and number2
sum = number1 + number2

# Difference between the two numbers
subtraction = number1 - number2

# Product of number1 and number2
product = number1 * number2

# Print the output of the operations to the console
print(f"Addition of {number1} and {number2} is {sum}")
print(f"Subtraction of {number1} and {number2} is {subtraction}")
print(f"Multiplication of {number1} and {number2} is {product}")