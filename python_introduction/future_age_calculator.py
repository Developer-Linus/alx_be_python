# This program prompts user for current age and calculates how old the user will be in future

# Define the variable by asking the user 
current_age = int(input("How old are you?"))

# Calculate how old the user will be in 2050 assuming current year is 2023
age = current_age + 27

# Print the output to the console
print(f"In 2050, you will be {age} years old.")